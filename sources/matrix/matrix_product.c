#include "matrix.h"

/*
** 00 01 02 03
** 10 11 12 13
** 20 21 22 23
** 30 31 32 33
*/
t_matrix		matrix_product(t_matrix const m1, t_matrix const m2)
{
	t_matrix	m3;
	int			i;
	int			j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			m3.m[i][j] = m1.m[i][0] * m2.m[0][j]
						+ m1.m[i][1] * m2.m[1][j]
						+ m1.m[i][2] * m2.m[2][j]
						+ m1.m[i][3] * m2.m[3][j];
			j++;
		}
		i++;
	}
	return (m3);
}
