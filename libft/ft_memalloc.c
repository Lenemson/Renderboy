#include <stdlib.h>
#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*mem;

	mem = malloc(size);
	if (mem == NULL)
		return (NULL);
	else
	{
		mem = ft_memset(mem, 0, size);
		return (mem);
	}
}
