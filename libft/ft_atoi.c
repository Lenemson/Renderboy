#include "libft.h"

int			ft_atoi(const char *str)
{
	int	nb;
	int	i;
	int	minus;

	nb = 0;
	i = 0;
	minus = 1;
	while (!ft_isprint(str[i]) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * minus);
}
